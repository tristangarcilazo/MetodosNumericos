    public vector calcular(double a,double b)
    {
        double a11,a12,a21,a22,f1,f2,det,x,y,aux1,aux2;
        a11=1+4*(a*b);
        a12=-a;
        a21=-2*(b*b);
        a22=2*a;
        f1=(a*a)+(a*b)-10;
        f2=b+(2*a*b*b)-57;
        det=1/(2*a+8*a*a*b-2*a*b*b);
        aux1=((a11*f1)+(a12*f2))*det;
        aux2=((a21*f1)+(a22*f2))*det;
        vector res=new vector(a-aux1,b-aux2);
        return res;
    }
    public class vector
    {
        double a1,a2;
        public vector(double a1, double a2) {
            this.a1 = a1;
            this.a2 = a2;
        }
        public double getA1() {
            return a1;
        }
        public double getA2() {
            return a2;
        }       
    }
