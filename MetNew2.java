    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!jTextField1.getText().equals(""))
        {
            jTextField1.setEnabled(false);
            vector dato=new vector(Double.parseDouble(jTextField1.getText()), Double.parseDouble(jTextField1.getText()));
            ArrayList<vector> lista=new ArrayList<>();
            do{
                lista.add(dato);
                dato=calcular(dato.getA1(), dato.getA2());
            }while(Math.abs(dato.getA1()-lista.get(lista.size()-1).getA1())>0.00001 && Math.abs(dato.getA2()-lista.get(lista.size()-1).getA2())>0.00001);
            String[][] resultados=new String[lista.size()][3];
            for(int i=0;i<lista.size();i++)
            {
                resultados[i][0]=Integer.toString(i);
                resultados[i][1]=Double.toString(fix(lista.get(i).getA1()));
                resultados[i][2]=Double.toString(fix(lista.get(i).getA2()));
            }
            jTable1.setModel(new javax.swing.table.DefaultTableModel(
                resultados,
                new String [] {
                    "n", "Xn", "Yn"
                }
            ));
        }
        else
        JOptionPane.showMessageDialog(rootPane,"Por favor ingrese un valor inicial", "Datos incompletos", JOptionPane.ERROR_MESSAGE);

    }//GEN-LAST:event_jButton1ActionPerformed

    public double fix(double x)
    {
        x*=10000;
        x=Math.round(x);
        return(x/10000);
    }
    
